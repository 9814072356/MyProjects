package battleship;

public class Vector2d {
    private double x;
    private double y;

    public Vector2d() {}

    public Vector2d(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
